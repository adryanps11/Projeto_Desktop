    private void btCriarpropostaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCriarpropostaActionPerformed
        Pedidos p = new Pedidos();
        int linha = tbMercado.getSelectedRow();
        p.setIdpedidos((int) tbMercado.getValueAt(linha, 0));
        p.setIdclientes((int) tbMercado.getValueAt(linha, 1));
        p.setDatapedidos((Date) tbMercado.getValueAt(linha, 2));
        p.setDataentrega((Date) tbMercado.getValueAt(linha, 3));

        List<Itenspedidos> itens = new ArrayList<>();

        int totalinha = tbLista.getRowCount();

        for (int j = 0; j < totalinha; j++) {
            Itenspedidos i = new Itenspedidos();
            i.setIdproduto((int) tbLista.getValueAt(j, 0));
            i.setProduto((String) tbLista.getValueAt(j, 1));
            i.setQuantidade((int) tbLista.getValueAt(j, 2));
            itens.add(i);
        }

        viewproposta v = new viewproposta(p, itens);
        v.setVisible(true);


    }//GEN-LAST:event_btCriarpropostaActionPerformed

