    private void tbMercadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMercadoMousePressed
        int linha = tbMercado.getSelectedRow();
        int cod = (int) tbMercado.getValueAt(linha, 0);
        DefaultTableModel lista = (DefaultTableModel) tbLista.getModel();
        lista.setNumRows(0);
        ctrlPedidos itenspedidos = new ctrlPedidos();
        List<Itenspedidos> itens = new ArrayList<>();
        try {
            itens = itenspedidos.ListarItensPedidos(cod);
            itens.forEach((p) -> {
                lista.addRow(new Object[]{
                    p.getIdproduto(),
                    p.getProduto(),
                    p.getQuantidade()

                });
            });
        } catch (SQLException ex) {
            Logger.getLogger(viewprincipal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_tbMercadoMousePressed

