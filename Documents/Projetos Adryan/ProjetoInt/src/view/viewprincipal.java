    private void AtualizarTbMercado() {
        DefaultTableModel mercadao = (DefaultTableModel) tbMercado.getModel();
        mercadao.setNumRows(0);
        PedidoDAO pedidos = new PedidoDAO();

        pedidos.obterPedidos().forEach((p) -> {
            mercadao.addRow(new Object[]{
                p.getIdpedidos(),
                p.getIdclientes(),
                p.getDatapedidos(),
                p.getDataentrega()
            });
        });
    }

